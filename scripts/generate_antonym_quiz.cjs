function generateAntonymQuestions() {
    // まず全学年の漢字リストを読み込む
    const allKanjiByGrade = {};
    const kanjiToGrade = {};
    for (let g = 1; g <= 6; g++) {
        const kanjiFile = path.join(kanjiDir, `grade${g}.json`);
        if (!fs.existsSync(kanjiFile)) continue;
        allKanjiByGrade[g] = JSON.parse(fs.readFileSync(kanjiFile, 'utf-8'));
        allKanjiByGrade[g].forEach(k => { kanjiToGrade[k.character] = g; });
    }

    for (let grade = 1; grade <= 6; grade++) {
        const quizFile = path.join(quizDir, `grade${grade}_quiz.json`);
        if (!fs.existsSync(quizFile)) continue;

        let quizData = JSON.parse(fs.readFileSync(quizFile, 'utf-8'));
        const kanjiData = allKanjiByGrade[grade];

        // 既存の対義語問題を削除（再生成する）
        const oldCount = quizData.filter(q => q.type === 'antonym').length;
        quizData = quizData.filter(q => q.type !== 'antonym');

        let addedCount = 0;
        let idCounter = Date.now();

        // 同学年 + 既習学年の漢字をプール
        const availableChars = [];
        for (let g = 1; g <= grade; g++) {
            if (allKanjiByGrade[g]) {
                availableChars.push(...allKanjiByGrade[g].map(k => k.character));
            }
        }

        kanjiData.forEach(kanji => {
            const char = kanji.character;
            const antonym = ANTONYMS[char];

            if (antonym && availableChars.includes(antonym)) {
                // 間違い選択肢を生成（同学年 + 既習学年から）
                const wrongChoices = availableChars
                    .filter(c => c !== char && c !== antonym)
                    .sort(() => Math.random() - 0.5)
                    .slice(0, 3);

                const choices = shuffleArray([antonym, ...wrongChoices]);
                quizData.push({
                    id: `g${grade}an${idCounter++}`,
                    type: 'antonym',
                    grade,
                    difficulty: grade <= 2 ? 1 : grade <= 4 ? 2 : 3,
                    question: `「${char}」の反対の意味の漢字は？`,
                    answer: antonym,
                    choices,
                    hint: `${kanjiToGrade[antonym]}年生で習う漢字です`,
                    category: '対義語',
                    targetKanji: char,
                });
                addedCount++;
            }
        });

        fs.writeFileSync(quizFile, JSON.stringify(quizData, null, 2), 'utf-8');
        console.log(`Grade ${grade}: Removed ${oldCount} old, added ${addedCount} antonym questions. Total: ${quizData.length}`);
    }
}

function shuffleArray(arr) {
    const a = [...arr];
    for (let i = a.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [a[i], a[j]] = [a[j], a[i]];
    }
    return a;
}

